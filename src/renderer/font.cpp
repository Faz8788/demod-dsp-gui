int Font::draw_char(Renderer& r, int x, int y, char ch, Color c, int scale) {
    int idx = (unsigned char)ch;
    if (idx >= 128) idx = '?';

    const uint8_t* glyph = FONT_DATA[idx];
    for (int row = 0; row < GLYPH_H; ++row) {
        uint8_t bits = glyph[row];
        for (int col = 0; col < GLYPH_W; ++col) {
            if (bits & (0x10 >> col)) {
                if (scale == 1) {
                    r.pixel(x + col, y + row, c);
                } else {
                    r.rect_fill(x + col * scale, y + row * scale,
                                scale, scale, c);
                }
            }
        }
    }
    return (GLYPH_W + SPACING) * scale;
}

int Font::draw_string(Renderer& r, int x, int y, const std::string& text,
                       Color c, int scale) {
    int cx = x;
    for (char ch : text) {
        cx += draw_char(r, cx, y, ch, c, scale);
    }
    return cx - x;
}

void Font::draw_centered(Renderer& r, int x, int y, int width,
                          const std::string& text, Color c, int scale) {
    int tw = measure(text, scale);
    draw_string(r, x + (width - tw) / 2, y, text, c, scale);
}

void Font::draw_right(Renderer& r, int right_x, int y,
                       const std::string& text, Color c, int scale) {
    int tw = measure(text, scale);
    draw_string(r, right_x - tw, y, text, c, scale);
}

int Font::measure(const std::string& text, int scale) {
    if (text.empty()) return 0;
    return int(text.size()) * (GLYPH_W + SPACING) * scale - SPACING * scale;
}

void Font::draw_glow(Renderer& r, int x, int y, const std::string& text,
                      Color c, Color glow, int scale) {
    // Draw glow layer (offset by 1px in each direction)
    Color g = { glow.r, glow.g, glow.b, 80 };
    draw_string(r, x - 1, y, text, g, scale);
    draw_string(r, x + 1, y, text, g, scale);
    draw_string(r, x, y - 1, text, g, scale);
    draw_string(r, x, y + 1, text, g, scale);

    // Draw main text on top
    draw_string(r, x, y, text, c, scale);
}

